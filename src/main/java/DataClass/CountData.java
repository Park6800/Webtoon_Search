package DataClass;

public class CountData {
	private String ID;
	private String TITLE;
	private String LIKE;
	private int CLICK;
	public String getID() {
		return ID;
	}
	public void setID(String ID) {
		this.ID = ID;
	}
	public String getTITLE() {
		return TITLE;
	}
	public void setTITLE(String TITLE) {
		this.TITLE = TITLE;
	}
	public String getLIKE() {
		return LIKE;
	}
	public void setLIKE(String LIKE) {
		this.LIKE = LIKE;
	}
	public int getCLICK() {
		return CLICK;
	}
	public void setCLICK(int CLICK) {
		this.CLICK = CLICK;
	}
}
